			Physionomie et attitudes
			Il ressemble à un jeune yorkshire. Son pelage est principalement court et marron, avec une zone plus sombre sur le dos. Son visage est recouvert d'une masse de poils plus clairs ébouriffés.

			Différences mâle/femelle
			Il n’y a aucune différence entre les individus mâles et femelles de cette espèce.

			Chromatique
			Lorsqu'il est chromatique, Ponchiot a un teint plus clair que les Ponchiot normaux, ses yeux et son pelage dorsal sont verts.

			Évolution
			Ponchiot évolue à partir du niveau 16 en Ponchien, et ce dernier évolue en Mastouffe à partir du niveau 32.
